export abstract class ColorMap {
  constructor(protected minVal: number = 0, protected maxVal: number = 1) {}
  abstract getColor(value: number): string;
}

type ColorsList = [number, number, number][];

export class RainbowColorMap extends ColorMap {
  getColor(value: number): string {
    const _value = Math.max(Math.min(value, this.maxVal), this.minVal);
    const ratio = (_value - this.minVal) / (this.maxVal - this.minVal);
    const step = Math.min(3, Math.floor(4 * ratio));
    const strength = Math.floor(255 * (ratio * 4 - step));
    let r = 0,
      g = 0,
      b = 0;
    switch (step) {
      case 0:
        r = 0;
        g = strength;
        b = 255;
        break;
      case 1:
        r = 0;
        g = 255;
        b = 255 - strength;
        break;
      case 2:
        r = strength;
        g = 255;
        b = 0;
        break;
      case 3:
        r = 255;
        g = 255 - strength;
        b = 0;
        break;
    }
    return rgbaToHex({ r, g, b, a: 1 });
  }
}

class UniformColorMap extends ColorMap {
  private color: { r: number; g: number; b: number; a: number };
  constructor(minVal: number = 0, maxVal: number = 1, color: string = "#000000") {
    super(minVal, maxVal);
    this.color = colorToRGBA(color);
  }
  getColor(value: number): string {
    const ratio = (value - this.minVal) / (this.maxVal - this.minVal);
    return rgbaToHex({ ...this.color, a: ratio });
  }
}

export class GreysColorMap extends UniformColorMap {
  constructor(minVal: number = 0, maxVal: number = 1) {
    super(minVal, maxVal, "#000000");
  }
}

class ListColorMap extends ColorMap {
  constructor(minVal: number = 0, maxVal: number = 1, private colors: ColorsList) {
    super(minVal, maxVal);
  }

  getColor(value: number): string {
    const ratio = Math.floor(
      ((this.colors.length - 1) * (value - this.minVal)) / (this.maxVal - this.minVal)
    );
    const [r, g, b] = this.colors[Math.max(0, Math.min(ratio, this.colors.length - 1))];
    return rgbaToHex({ r, g, b, a: 1 });
  }
}

