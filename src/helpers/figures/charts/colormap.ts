export class ThreePointsColorMap extends ColorMap {
  private minColor: RGBA;
  private midColor: RGBA;
  private maxColor: RGBA;
  constructor(
    minVal: number = 0,
    maxVal: number = 1,
    minColor: string = "#000000",
    midColor: string = "#ffffff",
    maxColor: string = "#000000"
  ) {
    super(minVal, maxVal);
    this.minColor = colorToRGBA(minColor);
    this.midColor = colorToRGBA(midColor);
    this.maxColor = colorToRGBA(maxColor);
  }
  getColor(value: number): string {
    const ratio = (value - this.minVal) / (this.maxVal - this.minVal);
    if (ratio < 0.5) {
      return rgbaToHex({
        r: Math.floor(this.minColor.r + (this.midColor.r - this.minColor.r) * (ratio * 2)),
        g: Math.floor(this.minColor.g + (this.midColor.g - this.minColor.g) * (ratio * 2)),
        b: Math.floor(this.minColor.b + (this.midColor.b - this.minColor.b) * (ratio * 2)),
        a: 1,
      });
    } else {
      const midRatio = (ratio - 0.5) * 2;
      return rgbaToHex({
        r: Math.floor(this.midColor.r + (this.maxColor.r - this.midColor.r) * midRatio),
        g: Math.floor(this.midColor.g + (this.maxColor.g - this.midColor.g) * midRatio),
        b: Math.floor(this.midColor.b + (this.maxColor.b - this.midColor.b) * midRatio),
        a: 1,
      });
    }
  }
}

class ListColorMap extends ColorMap {
  constructor(minVal: number = 0, maxVal: number = 1, private colors: ColorsList) {
    super(minVal, maxVal);
  }

  getColor(value: number): string {
    const ratio = Math.floor(
      ((this.colors.length - 1) * (value - this.minVal)) / (this.maxVal - this.minVal)
    );
    const [r, g, b] = this.colors[Math.max(0, Math.min(ratio, this.colors.length - 1))];
    return rgbaToHex({ r, g, b, a: 1 });
  }
}

